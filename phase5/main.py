"""
serving/main.py
────────────────
VeriTune FastAPI application.

Endpoints
---------
  POST /predict              → Route ticket, run LoRA inference, apply safety filters
  GET  /health               → Liveness + readiness check
  GET  /metrics              → Prometheus metrics (text/plain) + JSON snapshot
  GET  /router/calibrate     → Trigger router re-calibration from latest val data
  POST /admin/reload-adapters → Hot-reload adapter registry from checkpoint manager
  GET  /docs                 → Swagger UI (auto-generated by FastAPI)

Startup
-------
  uvicorn serving.main:app --host 0.0.0.0 --port 8000 --workers 2

Example
-------
  curl -X POST http://localhost:8000/predict \\
    -H "Content-Type: application/json" \\
    -d '{"ticket_text": "My headphones won'\''t charge after the firmware update."}'
"""

from __future__ import annotations

import logging
import os
import time
import uuid
from contextlib import asynccontextmanager
from typing import List, Optional

from fastapi import FastAPI, HTTPException, Request, Response, BackgroundTasks
from fastapi.middleware.cors import CORSMiddleware
from fastapi.responses import JSONResponse, PlainTextResponse

from routing.models import TicketRequest, TicketResponse, Domain
from serving.monitoring import setup_structured_logging, get_metrics, RequestTrace

logger = logging.getLogger(__name__)

# ── Application state (loaded once at startup) ─────────────────────────────────

_pipeline = None      # InferencePipeline singleton


def get_pipeline():
    global _pipeline
    if _pipeline is None:
        raise HTTPException(status_code=503, detail="Inference pipeline not initialised")
    return _pipeline


# ── Lifespan (startup + shutdown) ──────────────────────────────────────────────

@asynccontextmanager
async def lifespan(app: FastAPI):
    """Initialise all resources at startup; clean up at shutdown."""
    global _pipeline
    setup_structured_logging(level=os.getenv("LOG_LEVEL", "INFO"))
    logger.info("VeriTune starting up...")

    router_path   = os.getenv("ROUTER_PATH",   "outputs/router")
    registry_path = os.getenv("REGISTRY_PATH", "outputs/checkpoints/checkpoint_registry.json")
    device        = os.getenv("DEVICE",        "cpu")

    try:
        from serving.inference import build_pipeline
        _pipeline = build_pipeline(
            router_path=router_path,
            registry_path=registry_path,
            device=device,
        )
        # Warm up adapter cache on startup to minimise first-request latency
        _pipeline.warm_up()
        logger.info("VeriTune ready ✓")
    except Exception as e:
        logger.error("Pipeline init failed: %s", e, exc_info=True)
        # Don't crash the server — /health will report not ready

    yield   # application runs

    logger.info("VeriTune shutting down")
    _pipeline = None


